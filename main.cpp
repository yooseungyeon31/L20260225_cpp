
//정수2개 입력받고 5개의 산술연산자 활용하여 작성하기

#include <iostream>

using namespace std;

int main()
{

	int Number1= 0;
	int Number2 = 0;

	cout << "숫자 2개 입력: " << endl;
	cin>> Number1 >> Number2;

	cout <<"Number1 + Number2 =" << Number1 + Number2 << endl;
	cout << "Number1 * Number2 =" << Number1 * Number2 << endl;
	cout << "Number1 / Number2 =" << Number1 / Number2 << endl;
	cout << "Number1 % Number2 =" << Number1 % Number2 << endl;
	cout << "Number1 - Number2 =" << Number1 - Number2 << endl;




	return 0;
}